void main(){
  /// For Loop
  /// Using the forloop print the even numbers in range from 20 to 220
  /// 
  /// Step 1 : Create a for loop with initial value as 20 and end value as 220
  /// Step 2 : Check if the value is even or not
  /// Step 3 : store the even value in a list
  /// Step 4 : Print the list
  
  for(int i = 20; i <= 220; i++){
    if(i % 2 == 0){
      print(i);
    }
  }
}