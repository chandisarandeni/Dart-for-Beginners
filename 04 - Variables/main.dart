// This program demonstrates how to use variables in Dart 
void main() {
  // Declare and initialize an integer variable 'a'
  int a = 10;
  // Declare and initialize another integer variable 'b'
  int b = 12;

  // Print the value of 'a'
  print(a); // Outputs 10
  // Print the value of 'b'
  print(b); // Outputs 12

  // Declare and initialize a string variable 'name'
  String name = "Sample Name";
  // Print the value of 'name'
  print(name); // Outputs "Sample Name"

  // Declare and initialize a double variable 'sampleDouble'
  double sampleDouble = 12.5;
  // Print the value of 'sampleDouble'
  print(sampleDouble); // Outputs 12.5

  // Declare and initialize a boolean variable 'sampleBoolean'
  bool sampleBoolean = true;
  // Print the value of 'sampleBoolean'
  print(sampleBoolean); // Outputs true
}
