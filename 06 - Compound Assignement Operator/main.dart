void main() {
  int a = 10;

  print(a);

  a--; // Decrement operator
  print(a);

  //Addition (add 5 into a)
  a += 5; // a = a + 5;
  print(a);

  // Subtraction (subtract 3 from a)
  a -= 3; // a = a - 3;
  print(a);

  // Multiplication (multiply a by 2)
  a *= 2; // a = a * 2;
  print(a);

  // Division (divide a by 2)
  a ~/= 2; // Integer division operator
  print(a);
}
