void main() {
  int a = 10;

  // Print the initial value of 'a' 
  print(a); // Outputs 10

  // Decrement operator (subtract 1 from 'a')
  a--; // a = a - 1
  print(a); // Outputs 9

  // Addition (add 5 to 'a')
  a += 5; // a = a + 5
  print(a); // Outputs 14

  // Subtraction (subtract 3 from 'a')
  a -= 3; // a = a - 3
  print(a); // Outputs 11

  // Multiplication (multiply 'a' by 2)
  a *= 2; // a = a * 2
  print(a); // Outputs 22

  // Integer division (divide 'a' by 2)
  a ~/= 2; // a = a ~/ 2 (integer division, no decimals)
  print(a); // Outputs 11
}
