void main() {
  // This is about how arithmetic operators in dart

  // Addition of a String
  // We can add string in dart but we cannot substract strings
  print("100" + "100");

  //Addition
  print(10 + 12);

  //Substraction
  print(10 - 2);

  //Multiplication
  print(10 * 2);

  //Division
  print(10 / 3);

  //Mod
  print(10 % 3);
}
