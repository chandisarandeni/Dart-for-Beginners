void main() {
  // Declare an integer variable 'number' and assign it a value of 12
  int number = 12;

  // Check if the number is greater than 0
  if (number > 0) {
    // If true, print "Positive"
    print("Positive");
  } 
  // Check if the number is less than 0
  else if (number < 0) {
    // If true, print "Negative"
    print("Negative");
  } 
  // If the number is neither greater than 0 nor less than 0, it must be 0
  else {
    // Print "Zero"
    print("Zero");
  }
}
