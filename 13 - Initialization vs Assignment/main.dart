void main() {
  // Initialization
  int a = 10;       // Initialize integer variable 'a' with value 10
  String b = "Saman"; // Initialize string variable 'b' with value "Saman"

  // Assignment
  a = 200; // Reassign 'a' with the value 200
  print(a); // Outputs 200

  b = "Flutter"; // Reassign 'b' with the value "Flutter"
  print(b); // Outputs "Flutter"
}
