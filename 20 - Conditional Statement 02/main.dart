void main() {
  int waterLevel = 10; // Initialize water level to 10

  // Check if water level is 10
  if (waterLevel == 10) {
    print("Light the PINK BULB"); // Action for water level 10
  }
  
  // Check if water level is 20
  if (waterLevel == 20) {
    print("Light the BLUE BULB"); // Action for water level 20
  }
  
  // Check if water level is 30
  if (waterLevel == 30) {
    print("Light the GREEN BULB"); // Action for water level 30
  }
  
  // Note: Only the condition for water level 10 will be true in this example,
  // so only "Light the PINK BULB" will be printed.
}