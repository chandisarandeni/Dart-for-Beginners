void main() {
  // Declare and initialize a variable 'number' with a value of 10
  int number = 10;

  // Check if the number is greater than 0
  if (number > 0) {
    // Print "Positive" if the number is greater than 0
    print("Positive");
  } else {
    // Print "Zero or Negative" if the number is 0 or less
    print("Zero or Negative");
  }
}