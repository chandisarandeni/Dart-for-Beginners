void main() {
  // Declare and initialize a string variable 'firstName' 
  String firstName = "David";
  // Print the value of 'firstName'
  print(firstName); // Outputs "David"

  // Declare and initialize an integer variable 'myHeight' representing height in cm
  int myHeight = 156;
  // Print the value of 'myHeight'
  print(myHeight); // Outputs 156

  // Declare and initialize a double variable 'myWeight' representing weight in kg
  double myWeight = 56.5;
  // Print the value of 'myWeight'
  print(myWeight); // Outputs 56.5

  // Declare and initialize a boolean variable 'myBoolean'
  bool myBoolean = true;
  // Print the value of 'myBoolean'
  print(myBoolean); // Outputs true
}
